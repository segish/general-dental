<p>
    ➤ A <strong>High gradient</strong> (>1.1 g/dL) indicates that the ascites is due to Portal Hypertension, either
    liver-related or non-liver-related. These include Cirrhosis of the Liver, Heart failure, Budd-Chiari syndrome,
    Portal vein thrombosis, and Idiopathic portal fibrosis.
</p>
<p>
    ➤ A <strong>Low gradient</strong> (<1.1 g/dL) indicates causes of ascites not associated with increased portal
        pressure. This includes Tuberculosis, Pancreatitis, Infections, Serositis, Pulmonary infarcts, and Peritonitis.
        </p>
